    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        try {
            if (bookingId == null) {
                JOptionPane.showMessageDialog(this, "Please select data to delete");
            } else {
                int confirmation = JOptionPane.showConfirmDialog(
                        this,
                        "Are you sure you want to delete this Booking?",
                        "Confirmation",
                        JOptionPane.YES_NO_OPTION
                );

                if (confirmation == JOptionPane.YES_OPTION) {
                    Registry rg = LocateRegistry.getRegistry("127.0.0.1", 6000);
                    BookingService service = (BookingService) rg.lookup("bookingService");
                    Booking booking = new Booking();
                    booking.setBookingId(bookingId);

                    String message = service.deleteBooking(booking);
                    if (message != null) {
                        JOptionPane.showMessageDialog(this, message);
                        tbm.setRowCount(0);
                        addrows();
                       
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton7ActionPerformed

