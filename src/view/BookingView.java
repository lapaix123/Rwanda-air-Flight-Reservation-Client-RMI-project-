    private void dataTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataTblMouseClicked
        int index = dataTbl.getSelectedRow();
        TableModel tbm = dataTbl.getModel();
        bookingId = Long.parseLong(tbm.getValueAt(index, 0).toString());
        //
        try {
            Booking booking = new Booking();
            booking.setBookingId(bookingId);

            Registry rg = LocateRegistry.getRegistry("127.0.0.1", 6000);
            BookingService service = (BookingService) rg.lookup("bookingService");

            Booking booking1 = service.findBookingById(booking);
            if (booking1 != null) {
                bookingId = booking1.getBookingId();
                numberTxt.setText(booking1.getNumberOfbook().toString());

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_dataTblMouseClicked

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {

            Registry rg = LocateRegistry.getRegistry("127.0.0.1", 6000);
            BookingService service = (BookingService) rg.lookup("bookingService");
            
            Booking booking= new Booking();
            
            String flightInfo = flightCombo.getSelectedItem().toString();
            Long flightid = Long.parseLong(flightInfo.substring(0, flightInfo.indexOf("-")));
            Flight flight= new Flight();
            flight.setFlightId(flightid);
            booking.setFlight(flight);
            booking.setNumberOfbook(Integer.parseInt(numberTxt.getText()));
            
            String passengerInfo = flightCombo.getSelectedItem().toString();
            Long p = Long.parseLong(passengerInfo.substring(0, passengerInfo.indexOf("-")));
            Passenger passenger= new Passenger();
            passenger.setPassengerId(p);
            booking.setPassenger(passenger);
            booking.setBookingaDAte(new Date());
            booking.setStatus(statusCombo.getSelectedItem().toString());
            

            String message = service.createBooking(booking);
            if (message != null) {
                JOptionPane.showMessageDialog(this, message);
                tbm.setRowCount(0);
                addrows();
                
            }
        

    }
    catch (Exception e

    
        ) {
            e.printStackTrace();
    }
    }//GEN-LAST:event_saveBtnActionPerformed

